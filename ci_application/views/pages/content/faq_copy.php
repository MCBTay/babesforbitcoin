				<h3>What are 51% attacks?</h3>
				<div>
					<p>You can read about a 51% attack here (<a href="http://learncryptography.com/51-attack/" target="_blank">http://learncryptography.com/51-attack/</a>) however to sum it up; it could give the controlling user the power to create counterfeit Bitcoins, which could ruin the value of Bitcoin.  Alternatively, if someone finds the Bitcoins you have purchased were counterfeited, your personal purchase would be lost.</p>
