				<h3>How do I mine Bitcoin?</h3>
				<div>
					<p><a href="http://startbitcoin.com/" target="_blank">http://startbitcoin.com/</a></p>
					<p>If you do plan on joining a mining pool, we recommend P2Pool (<a href="http://www.p2pool.org/" target="_blank">http://www.p2pool.org/</a>), this pool aims to keep bitcoin decentralized in order to prevent 51% attacks.</p>
					<p>GHash.IO (<a href="https://ghash.io/" target="_blank">https://ghash.io/</a>) is common as well, however it often has a high hashrate and I recommend going with another pool to prevent the possibility of any problematic attacks.</p>
